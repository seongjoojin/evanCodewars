import comp from "../comp";

test('comp Test', () => {
    expect(comp([121, 144, 19, 161, 19, 144, 19, 11],[11*11, 121*121, 144*144, 19*19, 161*161, 19*19, 144*144, 19*19])).toBe(true);
    expect(comp([51, 82, 96, 47],[2601, 6724, 9216, 2209])).toBe(true);
    expect(comp([85, 91, 36, 81, 95, 84, 15],[7225, 8281, 1296, 6561, 9025, 7057, 225])).toBe(false);
    expect(comp([88, 38],[7744, 1444])).toBe(true);
    expect(comp([53, 43, 67, 75, 31, 33, 2],[2809, 1849, 4490, 5625, 961, 1089, 4])).toBe(false);
    expect(comp([92, 57, 18],[8464, 3250, 324])).toBe(false);
    expect(comp([41, 20, 65, 61],[1681, 401, 4225, 3721])).toBe(false);
    expect(comp([22, 63, 40, 58, 48],[484, 3969, 1600, 3364, 2304])).toBe(true);
});